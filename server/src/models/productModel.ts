import { InferSchemaType, Schema, model } from "mongoose"

const productSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  stock: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
})

type Product = InferSchemaType<typeof productSchema>

export default model<Product>("Product", productSchema)
